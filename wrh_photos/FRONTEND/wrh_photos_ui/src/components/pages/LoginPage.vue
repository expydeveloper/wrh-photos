<script>
import UtilMixin from "../mixins/UtilMixin";
import SessionApi from "../../endpoint/SessionApi"

export default {
  data: function() {
    return {
      signinForm: {},
      signingIn: false,
      pageState: "signin"
    };
  },
  mixins: [UtilMixin],
  methods: {
    signIn: function() {
      let self = this;
      this.signingIn = true;
      SessionApi.singIn(this.signinForm).then(
        function(response) {
          self.signinForm = {};
          self.$store.state.currentUser = response.data;
          self.signingIn = false;
          self.$router.replace({ name: self.$rns.ROOT });
          self.showSuccess("Welcome to WRH/Photos!", 5000);
        },
        function(response) {
          self.signingIn = false;
          self.showDefaultServerError(response, true);
        }
      );
    }
  },
  computed: {
    inSigninState: function() {
      return this.pageState === "signin";
    },
    inSignupState: function() {
      return this.pageState === "signup";
    },
    inForgetPasswordState: function() {
      return this.pageState === "forget_password";
    }
  },
  created: function() {
    if (this.$store.getters.isLoadedUser) {
      this.$router.replace({ name: this.$rns.ROOT });
    }
  }
};
</script>

